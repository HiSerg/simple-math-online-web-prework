def addition(num1, num2)
  num1 = num1 + num2
  return num1
end

def subtraction(num1, num2)
  num1 = num1 - num2
  return num1
end

def division(num1, num2)
  num1 = num1 / num2
  return num1
end

def multiplication(num1, num2)
  num1 = num1 * num2
  return num1
end

def modulo(num1, num2)
  num1 = num1 % num2
  return num1
end

def square_root(num)
  Integer.sqrt(num)
end

def order_of_operation(num1, num2, num3, num4)
  num1 = num1 + ((num2 * num3 ) / num4 )
  return num1
end
